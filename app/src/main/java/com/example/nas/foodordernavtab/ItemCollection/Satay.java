public class Satay {

    String namaSatay;
    Double hargaSatay;

    public Satay(String namaSatay, Double hargaSatay) {
        this.namaSatay = namaSatay;
        this.hargaSatay = hargaSatay;
    }

    public String getNamaSatay() {
        return namaSatay;
    }

    public Double getHargaSatay() {
        return hargaSatay;
    }
}
